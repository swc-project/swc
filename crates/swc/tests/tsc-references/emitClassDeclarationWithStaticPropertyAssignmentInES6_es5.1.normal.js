function _classCallCheck(instance, Constructor) {
    if (!(instance instanceof Constructor)) {
        throw new TypeError("Cannot call a class as a function");
    }
}
var C = function C() {
    "use strict";
    _classCallCheck(this, C);
};
C.z = "Foo";
var D = function D() {
    "use strict";
    _classCallCheck(this, D);
    this.x = 2e4;
};
D.b = true;
