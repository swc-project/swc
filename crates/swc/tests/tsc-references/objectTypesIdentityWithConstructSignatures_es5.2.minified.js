function _classCallCheck(instance, Constructor) {
    if (!(instance instanceof Constructor)) throw new TypeError("Cannot call a class as a function");
}
var A = function(x) {
    "use strict";
    _classCallCheck(this, A);
}, B = function(x) {
    "use strict";
    _classCallCheck(this, B);
}, C = function(x) {
    "use strict";
    _classCallCheck(this, C);
};
